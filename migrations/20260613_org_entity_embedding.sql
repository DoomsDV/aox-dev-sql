PROMPT === 2) Indice vectorial (opcional si el tenant lo soporta) ===

BEGIN
    EXECUTE IMMEDIATE q'[
        CREATE VECTOR INDEX idx_oee_embedding_vec
            ON org_entity_embedding (embedding)
            ORGANIZATION NEIGHBOR PARTITIONS
            DISTANCE COSINE
            WITH TARGET ACCURACY 95
    ]';
    DBMS_OUTPUT.PUT_LINE('Indice vectorial creado.');
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Indice vectorial omitido: ' || SQLERRM);
END;
