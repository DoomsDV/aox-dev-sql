-- Embeddings por organizacion para busqueda vectorial (cita rapida por voz, fase 1).
-- Requiere Oracle 23ai+ / Autonomous 26ai con tipo VECTOR.

PROMPT === 1) Tabla org_entity_embedding ===

BEGIN
    EXECUTE IMMEDIATE q'[
        CREATE TABLE org_entity_embedding (
            id_embedding        NUMBER GENERATED BY DEFAULT AS IDENTITY,
            org_id_organization NUMBER NOT NULL,
            entity_type         VARCHAR2(30) NOT NULL,
            entity_id           NUMBER NOT NULL,
            source_text         VARCHAR2(1000) NOT NULL,
            embedding           VECTOR(1536, FLOAT32) NOT NULL,
            updated_at          TIMESTAMP(6) WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
            CONSTRAINT pk_org_entity_embedding PRIMARY KEY (id_embedding),
            CONSTRAINT uq_org_entity_embedding UNIQUE (org_id_organization, entity_type, entity_id),
            CONSTRAINT fk_oee_org FOREIGN KEY (org_id_organization)
                REFERENCES organization (id_organization),
            CONSTRAINT chk_oee_entity_type CHECK (
                entity_type IN ('CUSTOMER', 'PROFESSIONAL', 'SERVICE', 'LOCATION')
            )
        )
    ]';
    DBMS_OUTPUT.PUT_LINE('org_entity_embedding creada.');
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE = -955 THEN
            DBMS_OUTPUT.PUT_LINE('org_entity_embedding ya existe.');
        ELSE
            RAISE;
        END IF;
END;
/

