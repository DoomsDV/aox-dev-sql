DECLARE
    v_orphan_pro NUMBER;
    v_orphan_ses NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_orphan_pro
      FROM professional p
      LEFT JOIN org_member m ON m.id_org_member = p.usr_id_user
     WHERE m.id_org_member IS NULL;

    SELECT COUNT(*) INTO v_orphan_ses
      FROM app_user_session s
      LEFT JOIN org_member m ON m.id_org_member = s.use_id_user
     WHERE m.id_org_member IS NULL;

    IF v_orphan_pro > 0 OR v_orphan_ses > 0 THEN
        RAISE_APPLICATION_ERROR(-20004,
            'Huerfanos tras migracion. professional=' || v_orphan_pro || ' sessions=' || v_orphan_ses);
    END IF;

    DBMS_OUTPUT.PUT_LINE('Post-validacion OK. Sin huérfanos.');
END;
