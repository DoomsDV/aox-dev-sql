DECLARE
    v_app_users   NUMBER;
    v_dup_email   NUMBER;
    v_orphans_pro NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_app_users FROM app_user;

    SELECT COUNT(*) INTO v_dup_email
      FROM (
        SELECT lower(email) e, COUNT(*) c
          FROM app_user
         GROUP BY lower(email)
        HAVING COUNT(*) > 1
      );

    IF v_dup_email > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Hay correos duplicados en app_user. Resolver manualmente antes de migrar.');
    END IF;

    SELECT COUNT(*) INTO v_orphans_pro
      FROM professional p
      LEFT JOIN app_user u ON u.id_user = p.usr_id_user
     WHERE u.id_user IS NULL;

    IF v_orphans_pro > 0 THEN
        RAISE_APPLICATION_ERROR(-20002, 'Profesionales sin app_user. Corregir antes de migrar.');
    END IF;

    DBMS_OUTPUT.PUT_LINE('app_user rows: ' || v_app_users);
    DBMS_OUTPUT.PUT_LINE('Pre-chequeos OK.');
END;
