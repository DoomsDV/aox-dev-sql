PROMPT === FASE 2: Copiar datos (IDs de membresia preservados) ===

DECLARE
    v_pu_count NUMBER;
    v_om_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_pu_count FROM platform_user;
    IF v_pu_count = 0 THEN
        INSERT INTO platform_user (
            apex_user_name, email, password_hash, first_name, last_name,
            is_active, email_verified_at, created_at
        )
        SELECT
            apex_user_name,
            lower(trim(email)),
            password_hash,
            first_name,
            last_name,
            is_active,
            email_verified_at,
            created_at
        FROM app_user;

        DBMS_OUTPUT.PUT_LINE('platform_user insertados: ' || SQL%ROWCOUNT);
    ELSE
        DBMS_OUTPUT.PUT_LINE('platform_user ya tiene datos, se omite INSERT.');
    END IF;

    SELECT COUNT(*) INTO v_om_count FROM org_member;
    IF v_om_count = 0 THEN
        INSERT INTO org_member (
            id_org_member,
            platform_user_id,
            org_id_organization,
            rol_id_role,
            is_active,
            created_at
        )
        SELECT
            au.id_user,
            pu.id_platform_user,
            au.org_id_organization,
            au.rol_id_role,
            au.is_active,
            au.created_at
        FROM app_user au
        INNER JOIN platform_user pu ON lower(pu.email) = lower(trim(au.email));

        DBMS_OUTPUT.PUT_LINE('org_member insertados: ' || SQL%ROWCOUNT);

        -- Alinear secuencia IDENTITY tras insertar IDs explícitos
        BEGIN
            EXECUTE IMMEDIATE '
                ALTER TABLE org_member MODIFY (
                    id_org_member GENERATED BY DEFAULT AS IDENTITY (START WITH LIMIT VALUE)
                )';
        EXCEPTION
            WHEN OTHERS THEN NULL;
        END;
    ELSE
        DBMS_OUTPUT.PUT_LINE('org_member ya tiene datos, se omite INSERT.');
    END IF;

    COMMIT;
END;
