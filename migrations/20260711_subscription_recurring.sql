PROMPT === 2. Columnas nuevas en org_subscription (idempotente) ===
DECLARE
    PROCEDURE add_col(pi_ddl IN VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE pi_ddl;
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE NOT IN (-1430) THEN  -- ORA-01430: la columna ya existe
                RAISE;
            END IF;
    END;
BEGIN
    add_col('ALTER TABLE org_subscription ADD (auto_renew NUMBER(1,0) DEFAULT 1 NOT NULL)');
    add_col('ALTER TABLE org_subscription ADD (last_charge_at TIMESTAMP(6) WITH TIME ZONE NULL)');
    add_col('ALTER TABLE org_subscription ADD (charge_retry_count NUMBER DEFAULT 0 NOT NULL)');
    BEGIN
        EXECUTE IMMEDIATE 'ALTER TABLE org_subscription ADD CONSTRAINT chk_orgsub_autorenew CHECK (auto_renew IN (0,1))';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE NOT IN (-2264, -2260) THEN  -- constraint ya existe
                RAISE;
            END IF;
    END;
END;
/

PROMPT === 3. Parametros de plataforma (pago recurrente / iframe uPay) ===
MERGE INTO app_parameter t
USING (
  SELECT 'PAGOPAR_RECURRENTE_BASE_URL'      AS param_key, 'Base URL de la API Pagopar pago-recurrente/3.0.'                         AS description, 'https://api.pagopar.com/api/pago-recurrente/3.0/' AS param_value FROM dual UNION ALL
  SELECT 'PAGOPAR_UPAY_IFRAME_URL'          AS param_key, 'URL base del iframe uPay de catastro (se le concatena el id-form).'      AS description, 'https://www.pagopar.com/upay-iframe/?id-form='  AS param_value FROM dual UNION ALL
  SELECT 'PAGOPAR_UPAY_RETURN_URL'          AS param_key, 'URL HTTPS donde se incrusta el iframe y a la que Pagopar redirige.'      AS description, 'https://hasel.app/panel/plan'                    AS param_value FROM dual UNION ALL
  SELECT 'SUBSCRIPTION_CARD_PROVIDER'       AS param_key, 'Proveedor de catastro de tarjeta (uPay o Bancard).'                      AS description, 'uPay'                                            AS param_value FROM dual UNION ALL
  SELECT 'SUBSCRIPTION_MAX_CHARGE_RETRIES'  AS param_key, 'Intentos maximos de cobro por ciclo antes de dejar de reintentar.'       AS description, '4'                                               AS param_value FROM dual
) s
ON (t.param_key = s.param_key)
WHEN NOT MATCHED THEN
  INSERT (param_key, param_value, description)
  VALUES (s.param_key, s.param_value, s.description);

COMMIT;

