PROMPT === 1. Tabla ORG_PAYMENT_CARD (idempotente) ===
DECLARE
    v_exists NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_exists FROM user_tables WHERE table_name = 'ORG_PAYMENT_CARD';
    IF v_exists = 0 THEN
        EXECUTE IMMEDIATE q'[
            CREATE TABLE org_payment_card (
              id_payment_card       NUMBER                      GENERATED BY DEFAULT AS IDENTITY,
              org_id_organization   NUMBER                      NOT NULL,
              provider              VARCHAR2(20)                DEFAULT 'uPay' NOT NULL,
              pagopar_identificador VARCHAR2(64)                NOT NULL,
              pagopar_card_id       VARCHAR2(64)                NULL,
              brand                 VARCHAR2(40)                NULL,
              masked_number         VARCHAR2(40)                NULL,
              card_type             VARCHAR2(20)                NULL,
              issuer                VARCHAR2(120)               NULL,
              status                VARCHAR2(20)                DEFAULT 'PENDING' NOT NULL,
              is_default            NUMBER(1,0)                 DEFAULT 0 NOT NULL,
              created_at            TIMESTAMP(6) WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
              confirmed_at          TIMESTAMP(6) WITH TIME ZONE NULL,
              updated_at            TIMESTAMP(6) WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL
            )]';
        EXECUTE IMMEDIATE 'CREATE INDEX idx_orgcard_org_status ON org_payment_card (org_id_organization, status)';
        EXECUTE IMMEDIATE 'ALTER TABLE org_payment_card ADD CONSTRAINT pk_org_payment_card PRIMARY KEY (id_payment_card)';
        EXECUTE IMMEDIATE 'ALTER TABLE org_payment_card ADD CONSTRAINT chk_orgcard_status CHECK (status IN (''PENDING'',''ACTIVE'',''DELETED''))';
        EXECUTE IMMEDIATE 'ALTER TABLE org_payment_card ADD CONSTRAINT chk_orgcard_default CHECK (is_default IN (0,1))';
        EXECUTE IMMEDIATE 'ALTER TABLE org_payment_card ADD CONSTRAINT fk_orgcard_org FOREIGN KEY (org_id_organization) REFERENCES organization (id_organization) ON DELETE CASCADE';
    END IF;
END;
